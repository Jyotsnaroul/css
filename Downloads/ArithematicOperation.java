class ArithematicOperation{
     public static void main(String args[]){
           int p=10;
           int q=20;
System.out.println(p+q);
System.out.println("p"+"q");
System.out.println("p+q");
System.out.println("p+q"+ p+q);
System.out.println("p-q"+ p+q);
System.out.println("p-q"+( p+q));
System.out.println("p*q"+( p*q));
System.out.println("@"+10+20);
System.out.println("@"+10+20);
System.out.println(10+"@"+20);
System.out.println(10+20+"@");
}
 }